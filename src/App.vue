      {
        title: 'Parallel Universe Roaming',
        description: 'Every choice creates a brand new parallel universe. Loop\'s unique "story tree" system visualizes all your decision paths. You can return to any key node at any time to explore another unfinished path, truly achieving full control over the story dimension and experiencing every possibility.'
      },
      {
        title: 'Interactive Co-director',
        description: 'All viewers can also participate in your life choices. AI automatically completes millions of branching storylines based on choices. Everyone is an audience, but also a creator and promoter of content.'
      }
    ])

    // Feature videos
    const capabilityVideos = ref([
      '01_Ingredients_Edit 1 16x9_250516d.mp4',
