  name: 'App',
  components: {
    Swiper,
    SwiperSlide
  },
  setup() {
    const heroCanvas = ref(null)
    const mainVideo = ref(null)
    const capabilitiesSwiper = ref(null)
    const isMainVideoPlaying = ref(false)
    const selectedCapability = ref(0)
    const showNavbar = ref(false)
    const isVideoMuted = ref(false)
    const shouldPlaySequence = ref(false)
    const currentVideoSrc = ref('视频资源新/loopai.mp4')
    const isPlayingReviewVideo = ref(false)

    // Features data
    const capabilities = ref([
      {
        title: 'AI Narrative Engine',
        description: 'Our self-developed AI narrative engine instantly understands your choice intentions. It integrates multimodal video generation, style rendering, and coherence maintenance technologies to calculate and generate the next unique storyline before you make your decision, breaking free from the linear constraints of traditional videos.'
