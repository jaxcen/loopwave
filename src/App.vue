      return `视频资源/${filename}`
    }

    const getNewVideoPath = (filename) => {
      return `视频资源新/${filename}`
    }

    const playMainVideo = () => {
      if (mainVideo.value) {
        // 重置到开始状态
        shouldPlaySequence.value = true
        isPlayingReviewVideo.value = false
        currentVideoSrc.value = '视频资源新/loopai.mp4'
        isMainVideoPlaying.value = true
        
        // 确保视频不循环
        mainVideo.value.loop = false
        
        // 等待视频源更新后再播放
        setTimeout(() => {
          if (mainVideo.value) {
            mainVideo.value.load() // 重新加载视频
            mainVideo.value.play()
          }
        }, 100)
      }
    }

    const onMainVideoEnded = () => {
      console.log('视频播放结束', { shouldPlaySequence: shouldPlaySequence.value, isPlayingReviewVideo: isPlayingReviewVideo.value })
      
      if (shouldPlaySequence.value && !isPlayingReviewVideo.value) {
        // 第一个视频 loopai.mp4 播放完毕，切换到 loop review.mov
        console.log('切换到 loop review.mov')
        isPlayingReviewVideo.value = true
        currentVideoSrc.value = '视频资源新/loop review.mov'
        
        // 等待视频源更新后播放
        setTimeout(() => {
          if (mainVideo.value) {
            mainVideo.value.load() // 重新加载视频
            mainVideo.value.play()
          }
        }, 100)
      } else if (shouldPlaySequence.value && isPlayingReviewVideo.value) {
        // loop review.mov 播放完毕，重新开始循环
        console.log('重新开始播放 loopai.mp4')
        isPlayingReviewVideo.value = false
        currentVideoSrc.value = '视频资源新/loopai.mp4'
        
        setTimeout(() => {
          if (mainVideo.value) {
            mainVideo.value.load() // 重新加载视频
            mainVideo.value.play()
          }
        }, 100)
      }
    }

    const selectCapability = (index) => {
      selectedCapability.value = index
    }

    const toggleMute = () => {
      isVideoMuted.value = !isVideoMuted.value
      if (mainVideo.value) {
        mainVideo.value.muted = isVideoMuted.value
      }
    }

    // Scroll listener to control navbar display
    const handleScroll = () => {
      const scrollTop = window.pageYOffset || document.documentElement.scrollTop
      // Show navbar when scrolled past 50% of viewport height
      showNavbar.value = scrollTop > window.innerHeight * 0.5
    }

    onMounted(() => {
      window.addEventListener('scroll', handleScroll)
    })

    onUnmounted(() => {
      window.removeEventListener('scroll', handleScroll)
    })

    return {
      heroCanvas,
      mainVideo,
      capabilitiesSwiper,
      isMainVideoPlaying,
      selectedCapability,
      showNavbar,
      capabilities,
      capabilityVideos,
      galleryColumns,
      getAssetPath,
      getVideoPath,
      getNewVideoPath,
      playMainVideo,
      selectCapability,
      isVideoMuted,
      toggleMute,
      shouldPlaySequence,
      currentVideoSrc,
      isPlayingReviewVideo,
      onMainVideoEnded
    }
